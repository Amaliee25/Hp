package slack.android.api.webapi.params;

/**
 * Set optional arguments to Slack Web Api Team integration logs method
 */
public class TeamIntegrationLogsParams extends Params{

    /**
     * Filter logs to this service. Defaults to all logs.
     *
     * @param serviceId
     */
    public void setServiceId(String serviceId){
        put(SlackParamsConstants.SERVICE_ID, serviceId);
    }

    /**
     * Filter logs to this Slack app. Defaults to all logs.
     *
     * @param appId
     */
    public void setAppId(String appId){
        put(SlackParamsConstants.APP_ID, appId);
    }

    /**
     * Filter logs generated by this userâ€™s actions. Defaults to all logs.
     * @param userId e.g. U1234567890
     */
    public void setUser(String userId){
        put(SlackParamsConstants.USER, userId);
    }

    /**
     * Filter logs with this change type. Defaults to all logs.
     *
     * @param changeType added
     */
    public void setChangeType(String changeType){
        put(SlackParamsConstants.CHANGE_TYPE, changeType);
    }

    /**
     * Number of items to return per page.
     *
     * Default: 100
     *
     * @param count e.g. 200
     */
    public void setCount(int count){
        put(SlackParamsConstants.COUNT, count, 1, 1000);
    }

    /**
     * Page number of results to return.
     *
     * Default: 1
     *
     * @param page e.g. 2
     */
    public void setPage(int page){
        put(SlackParamsConstants.PAGE, page, 1, 100);
    }
}
